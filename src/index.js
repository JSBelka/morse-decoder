function decode(expr) {
    let result = [];
    let letter = '';
    for (let i = 0; i < expr.length; i += 10) {
        letter = expr.substring(i, i + 10);
        result.push(letter);
    }
    let string = ';'
    result.map(item => {
        if(MORSE_TABLE_NUMBERS[item]) {
            string += MORSE_TABLE_NUMBERS[item]
        }
    });
    return string;
}

